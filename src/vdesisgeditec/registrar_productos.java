    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        int fila = tblproductos.getSelectedRow();
        tblproductos.setValueAt(txtidproducto.getText(), fila, 0);
        tblproductos.setValueAt(txtidproveedor.getText(), fila, 1);
        tblproductos.setValueAt(txtnombreproducto.getText(), fila, 2);
        tblproductos.setValueAt(txtmarca.getText(), fila, 3);
        tblproductos.setValueAt(txtpreciounidad.getText(), fila, 4);
        tblproductos.setValueAt(txtcantidadinventario.getText(), fila, 5);
    }//GEN-LAST:event_btnactualizarActionPerformed

