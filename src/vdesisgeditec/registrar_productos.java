    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
     Object idproducto = txtidproducto.getText();
     Object idproveedor = txtidproveedor.getText();
     Object nombreproducto= txtnombreproducto.getText();
     Object marca = txtmarca.getText();
     Object preciounidad = txtpreciounidad.getText();
     Object cantidadinventario = txtcantidadinventario.getText();
        
     ((DefaultTableModel) tblproductos.getModel()).addRow(new Object[]{idproducto ,idproveedor ,  nombreproducto, marca, preciounidad,cantidadinventario});

    }//GEN-LAST:event_btnregistrarActionPerformed

