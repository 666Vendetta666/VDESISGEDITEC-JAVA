    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
       int fila = tblproveedores.getSelectedRow();
        tblproveedores.setValueAt(txtidproveedor.getText(), fila, 0);
        tblproveedores.setValueAt(txtidproducto.getText(), fila, 1);
        tblproveedores.setValueAt(txtnombreproducto.getText(), fila, 2);
        tblproveedores.setValueAt(txtnombreproveedor.getText(), fila, 3);
        tblproveedores.setValueAt(txttelefonoproveedor.getText(), fila, 4);
    }//GEN-LAST:event_btnactualizarActionPerformed

