    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
     Object idproveedor = txtidproveedor.getText();
     Object idproducto = txtidproducto.getText();
     Object nombreproducto= txtnombreproducto.getText();
     Object nombreproveedor = txtnombreproveedor.getText();
     Object telefonoproveedor = txttelefonoproveedor.getText();
        
     ((DefaultTableModel) tblproveedores.getModel()).addRow(new Object[]{idproveedor, idproducto , nombreproducto, nombreproveedor,telefonoproveedor});
    }//GEN-LAST:event_btnregistrarActionPerformed

